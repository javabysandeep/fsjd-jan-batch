public class Demo1 {
    public static void main(String[] args) {
        //do while : repeat statement
        // iterations are not known in advance but code has to be executed at least once

        // for the first iterations condition will be not checked

        do {
            System.out.println("do while demo");
        } while (false);

    }
}
