package controlStatements.conditional.ifElseDemos;

// control statements
// conditional : if-else, switch : will help us to either execute the code or skip it.
// controlStatements.looping, repeat, iterative : for, while, do-while , foreach : will help us executing the code again and again
// transfer statements: break, continue, return: it will help us in transferring the control of executing the code from one place to another place.
public class ControlStatementsDemo1 {

    //control statements : will help us in controlling the flow of execution of the program.
    public static void main(String[] args) {
        System.out.println("*");//100 CR times repeat
        System.out.println("%%%%%%%");//skip this line
        System.out.println("^^^^^^^^");
        System.out.println("%%%%%%%");//execute this line
        System.out.println("%%%%%%%");
        System.out.println("%%%%%%%");

    }
}